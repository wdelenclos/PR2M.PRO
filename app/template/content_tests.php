                                            <p>&nbsp;</p>

                                            <h5><strong><span style="text-decoration: underline;">Consignes pour l&rsquo;exp&eacute;rimentateur&nbsp;:</span> </strong></h5>

                                            <p><strong>&nbsp;</strong>Lorsque l&rsquo;enfant a d&eacute;nomm&eacute; le mot&nbsp;:</p>

                                            <ul>

                                                <li><strong>Bonne r&eacute;ponse&nbsp;: </strong>appuyer sur la touche &rarr; de l&rsquo;ordinateur (cf groupe de fl&egrave;ches sur votre clavier)</li>

                                                <li><strong>Erreur ou non r&eacute;ponse&nbsp;: </strong>appuyer sur la touche &larr; de l&rsquo;ordinateur</li>

                                            </ul>

                                            <p>&nbsp;</p>

                                            <p><strong>Il est conseill&eacute; de s&rsquo;entra&icirc;ner &agrave; cette manipulation&nbsp;!</strong></p>

                                            <p>&nbsp;</p>

                                            <p><em>Si l&rsquo;enfant ne parvient pas &agrave; d&eacute;nommer l&rsquo;image apr&egrave;s environ 7 secondes, un <strong>indi&ccedil;age phon&eacute;mique</strong> lui est propos&eacute;. En cas d&rsquo;&eacute;chec, la <strong>premi&egrave;re syllabe</strong> est ensuite donn&eacute;e &agrave; l&rsquo;enfant pour l&rsquo;aider &agrave; r&eacute;cup&eacute;rer l&rsquo;&eacute;tiquette phonologique.</em></p>

                                            <p><em>&nbsp;</em></p>

                                            <p>ATTENTION&nbsp;:</p>

                                            <ul>

                                                <li>Pr&eacute;voir un enregistrement audio de toute la passation (ex. smartphone / GSM).</li>

                                                <li>Noter en parall&egrave;le les &eacute;ventuelles erreurs de l&rsquo;enfant sur le protocole (cf document &laquo;&nbsp;protocole_d&eacute;nomination_LA&nbsp;&raquo; &agrave; imprimer).</li>

                                            </ul>

                                            <p>&nbsp;</p>

                                            <p><em>L&rsquo;ordinateur enregistre automatiquement les temps de d&eacute;nomination du sujet. </em></p>

                                            <p>&nbsp;</p>

