                                            <hr>
                                            <h5><span style="text-decoration: underline;">T&acirc;che:</span></h5>
                                            <p>Choisir l'image qui correspond au mot propos&eacute; oralement (ex. balai) parmi 4 images.&nbsp;</p>
                                            <p>&nbsp;</p>
                                            <h5><span style="text-decoration: underline;">Consignes pour l'exp&eacute;rimentateur:</span></h5>
                                            <ul>
                                                <li>Ouvrir le fichier &laquo;&nbsp;EVIP_B.pptx&nbsp;&raquo;</li>
                                                <li>Lancer le diaporama</li>
                                                <li>Suivre la liste des items dans l&rsquo;ordre propos&eacute; (cf document &laquo; protocole_EVIPb.pdf &raquo;)
                                                    <ul>
                                                        <li><strong>Pour les enfants de 6&nbsp;ans : </strong>le point de d&eacute;part est <strong>l&rsquo;item 1</strong> (ruche)</li>
                                                        <li><strong>Pour les enfants de 7 ans :</strong> le point de d&eacute;part est <strong>l&rsquo;item 10</strong> (poignet)</li>
                                                        <li><strong>Pour les enfants de 8 ans :</strong> le point de d&eacute;part est <strong>l&rsquo;item 20</strong> (illumination)</li>
                                                    </ul>
                                                </li>
                                                <li>Ne donner AUCUN indice</li>
                                                <li>Ne donner AUCUN feedback</li>
                                                <li>Cette &eacute;preuve ne pr&eacute;sente pas de contrainte temporelle&nbsp;; l&rsquo;enfant prend le temps n&eacute;cessaire pour r&eacute;pondre</li>
                                                <li>Appuyer sur la barre &laquo;&nbsp;espace&nbsp;&raquo; pour passer &agrave; l&rsquo;item suivant</li>
                                                <li>Chronom&eacute;trer la dur&eacute;e de passation</li>
                                            </ul>

                                            <h5><span style="text-decoration: underline;"><strong>Cotation&nbsp;: </strong></span></h5>
                                            <ul>
                                                <li><strong>+ ou -&nbsp;: </strong>
                                                    <ul>
                                                        <li>+ si l&rsquo;item est correctement d&eacute;sign&eacute;</li>
                                                        <li>&ndash; si non r&eacute;ponse ou erreur &nbsp;(le cas &eacute;chant, noter la case choisie&nbsp;; &laquo;&nbsp;1&nbsp;&raquo;, &laquo;&nbsp;2&nbsp;&raquo;, &laquo;&nbsp;3&nbsp;&raquo;, &nbsp;ou &laquo;&nbsp;4&nbsp;&raquo;)</li>
                                                    </ul>
                                                </li>
                                                <li>Cette cotation doit se faire &agrave; la main (cf document &laquo;&nbsp;protocole_EVIPb.pdf&nbsp;&raquo;)
                                                    <ul>
                                                        <li>BASE&nbsp;: plus haute s&eacute;rie de 8 bonnes r&eacute;ponses cons&eacute;cutives</li>
                                                        <li>PLAFOND&nbsp;: plus basse s&eacute;rie de 8 r&eacute;ponses cons&eacute;cutives contenant 6 &eacute;checs</li>
                                                        <li>SCORE BRUT&nbsp;: num&eacute;ro de l&rsquo;item plafond <em>moins</em> les &eacute;checs</li>
                                                    </ul>
                                                </li>
                                            </ul>

                                            <h5><span style="text-decoration: underline;"><strong>R&eacute;cup&eacute;ration des donn&eacute;es&nbsp;: </strong></span></h5>
                                            <ul>
                                                <li>Renseigner manuellement dans la plateforme les r&eacute;sultats BASE, PLAFOND, et SCORE BRUT</li>
                                                <li>Scanner et enregistrer dans la plateforme le document &laquo;&nbsp;protocole_EVIPb.pdf&nbsp;&raquo;</li>
                                            </ul>


