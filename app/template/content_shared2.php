                        <h4 style="text-align: justify;"><strong><u>Fondement th&eacute;orique&nbsp;:</u></strong></h4>
                        <p style="text-align: justify;">Le Manque du Mot (MM) est un <strong>trouble de l&rsquo;&eacute;vocation lexicale</strong> d&eacute;fini comme un dysfonctionnement langagier touchant le versant expressif alors que le versant r&eacute;ceptif est pr&eacute;serv&eacute;. Cette <strong>dissociation</strong> de performances entre capacit&eacute;s de d&eacute;nomination et de d&eacute;signation est&nbsp; caract&eacute;ris&eacute;e par l&rsquo;impossibilit&eacute; pour le sujet d&rsquo;&eacute;voquer le mot qu&rsquo;il souhaite utiliser, quel que soit le contexte, et avec une impression intense de le conna&icirc;tre (Bonin, 2003).</p>
                        <p style="text-align: justify;">A ce jour les &eacute;tiologies du MM restent d&eacute;battues. Il existe <strong>trois hypoth&egrave;ses principales</strong> pour expliquer ce trouble&nbsp;:</p>
                        <ul style="text-align: justify;">
                        <li>Un <strong>d&eacute;ficit d&rsquo;acc&egrave;s / r&eacute;cup&eacute;ration</strong> (Dollaghan, 1987) ou <strong>de stockage</strong> (Kail et Leonard, 1986&nbsp;; McGregor et Leonard, 1989)</li>
                        <li>Un <strong>d&eacute;ficit des processus de repr&eacute;sentations phonologiques</strong> (Stackhouse, 1993&nbsp;; Constable et al., 1997) ou <strong>s&eacute;mantiques</strong> (McGregor, 1997&nbsp;; Lewis et Speake, 1998)</li>
                        <li>Un dysfonctionnement proc&eacute;dural caract&eacute;ris&eacute; par un <strong>d&eacute;ficit de la vitesse de traitement</strong> pouvant &ecirc;tre <strong>sp&eacute;cifiquement verbal ou non verbal </strong>(Miller et al., 2001&nbsp;; Ullman et Pierpont, 2005&nbsp;; Leonard et al., 2007)</li>
                        </ul>
                        <p style="text-align: justify;">L&rsquo;hypoth&egrave;se d&rsquo;un d&eacute;ficit s&eacute;mantique et/ou phonologique est aujourd&rsquo;hui la plus robuste et la plus document&eacute;e (Seiger-Gardner et Scwartz, 2008&nbsp;; Bragard, 2012). Cependant la litt&eacute;rature souligne des gains relatifs aux diff&eacute;rentes rem&eacute;diations &eacute;tudi&eacute;es, notamment en terme de g&eacute;n&eacute;ralisation (Schelstraete, 2011&nbsp;; Bragard, 2012).&nbsp;La question de l&rsquo;implication de la vitesse de traitement reste quant &agrave; elle d&eacute;battue et demeure la moins &eacute;tudi&eacute;e (Bragard, 2012&nbsp;; Trappeniers, 2015). <strong>Ce travail de m&eacute;moire aura pour but d&rsquo;explorer plus en profondeur cette troisi&egrave;me hypoth&egrave;se. </strong></p>
                        <p style="text-align: justify;">L&rsquo;id&eacute;e selon laquelle le d&eacute;ficit impliqu&eacute; dans le MM ne serait pas purement linguistique mais proc&eacute;dural nous para&icirc;t en effet pertinent dans un contexte o&ugrave; la sp&eacute;cificit&eacute; langagi&egrave;re des TSL est remise en cause (Ullman et Pierpont, 2005&nbsp;; Leonard et al., 2007). Davantage structurelle, cette hypoth&egrave;se serait donc <strong>plus proche du fonctionnement cognitif</strong>. Elle engage &eacute;galement la question de possibles <strong>aspects transf&eacute;rentiels</strong> entre langage et fonctions ex&eacute;cutives.</p>
                        <p style="text-align: justify;">En proposant <strong>un protocole simple d&rsquo;entra&icirc;nement &agrave; la vitesse d&rsquo;acc&egrave;s lexicale</strong>, cette recherche tentera d&rsquo;apporter des &eacute;l&eacute;ments de r&eacute;ponses aux relations causales entre vitesse de traitement et MM.</p>
                        <p style="text-align: justify;">S&rsquo;il est valid&eacute;, ce protocole permettra de proposer une rem&eacute;diation facilement applicable en pratique clinique.</p>
                        
