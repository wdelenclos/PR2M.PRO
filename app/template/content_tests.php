                                            <ul>
                                                <li>S&eacute;lectionner l&rsquo;&eacute;preuve &laquo;&nbsp;d&eacute;nomination de substantifs&nbsp;&raquo;</li>
                                                <li>Pr&eacute;voir un enregistrement audio de toute la passation</li>
