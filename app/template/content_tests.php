                                            <h5><span style="text-decoration: underline;"><strong>T&acirc;che&nbsp;:</strong></span></h5>
                                            <p>D&eacute;nommer le plus rapidement la liste de couleurs pr&eacute;sent&eacute;e</p>

                                            <h5><span style="text-decoration: underline;"><strong>Consignes&nbsp;pour l&rsquo;exp&eacute;rimentateur&nbsp;:</strong></span></h5>
                                            <ul>
                                                <li>Imprimer le fichier &laquo;&nbsp;Epreuve_d&eacute;nomination_rapide&nbsp;&raquo; (2 pages&nbsp;; ligne d&rsquo;essai + test)</li>
                                                <li>Imprimer la feuille de passation &laquo;&nbsp;protocole_d&eacute;nomination_rapide&nbsp;&raquo;</li>
                                                <li>Demander &agrave; l&rsquo;enfant de nommer le plus rapidement possible, mais en &eacute;vitant de faire des erreurs tous les &eacute;l&eacute;ments un par un (de gauche &agrave; droite, dans le sens de la lecture)</li>
                                                <li>Corriger les erreurs de l&rsquo;enfant SEULEMENT pour les essais (en gras sur la feuille de passation)</li>
                                                <li>Chronom&eacute;trer &agrave; partir du d&eacute;but du test (hors essai)</li>
                                                <li>Noter les erreurs, les &eacute;l&eacute;ments mal prononc&eacute;s ou omis</li>
                                            </ul>
                                            <p>&nbsp;</p>
                                            <h5><span style="text-decoration: underline;"><strong>R&eacute;cup&eacute;ration des donn&eacute;es&nbsp;:</strong></span></h5>
                                            <ul>
                                                <li>Renseigner manuellement dans la plateforme le temps de passation</li>
                                                <li>Scanner et enregistrer dans la plateforme la feuille de passation &laquo;&nbsp;protocole_d&eacute;nomination_rapide&nbsp;&raquo;</li>
                                            </ul>
