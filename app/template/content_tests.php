                                            <h4><strong><span style="text-decoration: underline;">2) Compr&eacute;hension lexicale</span>&nbsp;</strong></h4>

                                            <p><strong><span style="text-decoration: underline;">Consigne&nbsp;:</span></strong></p>

                                            <p>&laquo;&nbsp;Je vais te dire des mots et tu vas choisir la bonne image&nbsp;&raquo;</p>

                                            <p>&nbsp;</p>

                                            <p><em>Balayer du doigt la premi&egrave;re bande de dessins et dire le mot-cible&nbsp;: &laquo;&nbsp;Montre-moi <u>canard</u>&nbsp;&raquo; (sans dire l&rsquo;article). Et poursuivre un &agrave; un avec chaque bande. </em></p>

                                            <p><em>&nbsp;</em></p>

                                            <p><em>R&eacute;guli&egrave;rement si l&rsquo;enfant se pr&eacute;cipite, lui dire&nbsp;: &laquo;&nbsp;Prends ton temps, regarde bien toutes les images avant de r&eacute;pondre&nbsp;&raquo;. En aucun cas il ne faut corriger l&rsquo;enfant ou lui donner la bonne r&eacute;ponse. </em></p>

                                            <p><em>&nbsp;</em></p>

                                            <p>Commencer <strong>page 8</strong> (gu&eacute;pard et nager)&nbsp;:</p>

                                            <ul>

                                                <li>Si <strong>les deux r&eacute;ponses</strong> sont correctes, continuer l&rsquo;&eacute;preuve et compter 2 points pour gu&eacute;pard et nager et 12 points pour les images pr&eacute;c&eacute;dentes,</li>

                                                <li>Sinon, reprendre l&rsquo;&eacute;preuve &agrave; la <strong>page 2</strong> (canard et ballon)</li>

                                            </ul>

                                            <p>&nbsp;</p>

                                            <p>ATTENTION&nbsp;: pour toutes les classes, <strong>arr&ecirc;ter l&rsquo;&eacute;preuve apr&egrave;s 5 erreurs successives .</strong></p>

                                            <p><strong>&nbsp;</strong></p>

                                            <p><strong><span style="text-decoration: underline;">Cotation&nbsp;:</span> </strong></p>

                                            <ul>

                                                <li><strong>1 point&nbsp;:</strong> par d&eacute;signation correcte. Seule une auto-correction spontan&eacute;e et imm&eacute;diate donne 1 point</li>

                                                <li><strong>0 point&nbsp;:</strong> toute autre r&eacute;ponse</li>

                                            </ul>

