                                            <h4><strong><span style="text-decoration: underline;">1) Evocation Lexicale </span>&nbsp;</strong></h4>

                                            <p><strong><span style="text-decoration: underline;">Consigne&nbsp;: </span></strong></p>

                                            <p>Montrer les&nbsp; images, planche par planche et demander &agrave; l&rsquo;enfant&nbsp;: &laquo;&nbsp;Qu&rsquo;est-ce que c&rsquo;est&nbsp;?&nbsp;&raquo;</p>

                                            <p>&nbsp;</p>

                                            <p><em>Si l&rsquo;enfant donne une r&eacute;ponse qui n&rsquo;est pas celle attendue, syst&eacute;matiquement lui donner une seconde chance&nbsp;:&nbsp;&laquo;&nbsp;C&rsquo;est bien mais plus pr&eacute;cis&eacute;ment&nbsp;&raquo;. </em></p>

                                            <p><em>En aucun cas, il ne faut corriger l&rsquo;enfant ou lui donner la bonne r&eacute;ponse. </em></p>

                                            <p><em>&nbsp;</em></p>

                                            <p>Commencer <strong>page 5</strong>, par l&rsquo;item 11 (marron) puis l&rsquo;item 12 (pouce)&nbsp;:</p>

                                            <ul>

                                                <li>Si <strong>les deux r&eacute;ponses</strong> sont correctes, continuer l&rsquo;&eacute;preuve et compter 4 points pour marron et pouce et 20 points pour les images pr&eacute;c&eacute;dentes,</li>

                                                <li>Sinon, reprendre l&rsquo;&eacute;preuve, <strong>page 2</strong> &agrave; l&rsquo;item 1 (chaise)</li>

                                            </ul>

                                            <p>&nbsp;</p>

                                            <p>ATTENTION&nbsp;: pour toutes les classes, <strong>arr&ecirc;ter l&rsquo;&eacute;preuve apr&egrave;s 5 erreurs successives (cotation &agrave; 0). </strong>Seul le mot exact compte bon, m&ecirc;me s&rsquo;il est mal prononc&eacute; (par exemple <em>table</em> pour <em>bureau</em> est faux, mais <em>&eacute;cueil</em> pour <em>&eacute;cureuil</em> est bon).</p>

                                            <p>&nbsp;</p>

                                            <p><strong><span style="text-decoration: underline;">Cotation&nbsp;:</span></strong></p>

                                            <ul>

                                                <li><strong>2 points&nbsp;:</strong> si la r&eacute;ponse est correcte m&ecirc;me si mal prononc&eacute;e</li>

                                                <li><strong>1 point&nbsp;:</strong> si la deuxi&egrave;me r&eacute;ponse est correcte</li>

                                                <li><strong>0 point&nbsp;: </strong>toute autre r&eacute;ponse</li>

                                            </ul>

                                            <p>&nbsp;</p>

                                            <p><em>Pour plus de pr&eacute;cisions, se r&eacute;f&eacute;rer &agrave; la cotation relative &agrave; chaque planche (cf protocole de passation). </em></p>

