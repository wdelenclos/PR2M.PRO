                                        <div class="tab-pane" id="vtnv"><p class="lead">Test de Vitesse de Traitement Non Verbal (VTNV)&nbsp;</p>
                                            <h5>Nothelier et Delenclos, 2017&nbsp;</h5>

                                            <a class="btn btn-primary" href="data/tests_uploads/vtnv/Projet.pr2m-VTNV.zip" download="Projet.pr2m-VTNV.zip" target="_blank"><i class="fa fa-download"></i> Test VTNV</a>
                                            <p>&nbsp;</p>
                                            <h5><span style="text-decoration: underline;"><strong>T&acirc;che&nbsp;:</strong></span></h5>
                                            <p>L&rsquo;enfant doit appuyer sur la barre d&rsquo;espacement d&egrave;s qu&rsquo;il voit appara&icirc;tre une image au centre de l&rsquo;&eacute;cran</p>
                                            <p><em>L&rsquo;objectif de ce test est de calculer le temps de r&eacute;action non verbal de l&rsquo;enfant dans deux modalit&eacute;s&nbsp;: 1) avec une forme, 2) avec un symbole non s&eacute;mantisable</em></p>
                                            <p>&nbsp;</p>
                                            <h5><strong>1) Modalit&eacute; &laquo;&nbsp;Forme&nbsp;&raquo;</strong></h5>
                                            <p><strong><span style="text-decoration: underline;">Consignes pour l&rsquo;exp&eacute;rimentateur&nbsp;:</span> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
                                            <ul>
                                                <li>Ouvrir l&rsquo;application</li>
                                                <li>Remplir les champs demand&eacute;s&nbsp;:
                                                    <ul>
                                                        <li>Nom</li>
                                                        <li>Pr&eacute;nom</li>
                                                        <li>Nombre de r&eacute;p&eacute;tition&nbsp;: <strong>40 </strong></li>
                                                        <li>Intervalle minimum&nbsp;: <strong>1500 </strong></li>
                                                        <li>Intervalle maximum&nbsp;: <strong>6000 </strong></li>
                                                        <li>Image&nbsp;: <strong>FORME </strong></li>
                                                    </ul>
                                                </li>
                                                <li>Cliquer sur &laquo;&nbsp;Valider&nbsp;&raquo; <em>l&rsquo;enfant aura alors 5 secondes avant le d&eacute;but du test </em></li>
                                            </ul>
                                            <p>&nbsp;</p>
                                            <p><span style="text-decoration: underline;"><strong>Consignes pour l&rsquo;enfant (avant d&rsquo;appuyer sur &laquo;&nbsp;Valider&nbsp;&raquo;)&nbsp;:</strong></span></p>
                                            <p><strong>&laquo;&nbsp;</strong>Le test va commencer dans 5 secondes. Tu vas voir plusieurs fois une image appara&icirc;tre au centre de l&rsquo;&eacute;cran, &ccedil;a sera toujours la m&ecirc;me. D&egrave;s que tu la vois, tu appuies sur la touche espace (lui montrer). Il faut &ecirc;tre le plus rapide possible mais attention&nbsp;! si tu appuies avant de la voir, &ccedil;a t&rsquo;enl&egrave;ve des points.&nbsp;Tu as compris&nbsp;? Tu es pr&ecirc;t&nbsp;?&nbsp;&raquo;</p>
                                            <p>&nbsp;</p>
                                            <p><span style="text-decoration: underline;"><strong>Cotation&nbsp;: </strong></span></p>
                                            <ul>
                                                <li>Un fichier avec l&rsquo;ensemble des donn&eacute;es est g&eacute;n&eacute;r&eacute; automatiquement</li>
                                            </ul>
                                            <p><strong>&nbsp;</strong></p>
                                            <h5><strong>&nbsp;2)&nbsp;</strong><strong>Modalit&eacute; &laquo;&nbsp;Symbole non s&eacute;mantisable&nbsp;&raquo;</strong></h5>
                                            <p><span style="text-decoration: underline;"><strong>Consignes pour l&rsquo;exp&eacute;rimentateur&nbsp;: </strong></span></p>
                                            <ul>
                                                <li>M&ecirc;me protocole mais pour l&rsquo;onglet IMAGE s&eacute;lectionner <strong>Symbole non s&eacute;mantisable</strong></li>
                                            </ul>
                                            <p><strong>&nbsp;</strong></p>
                                            <p><span style="text-decoration: underline;"><strong>Consignes pour l&rsquo;enfant (avant d&rsquo;appuyer sur &laquo;&nbsp;Valider&nbsp;&raquo;)&nbsp;:</strong></span></p>
                                            <ul>
                                                <li>M&ecirc;me consigne</li>
                                                <li>Pr&eacute;venir l&rsquo;enfant qu&rsquo;il va voir une image diff&eacute;rente cette fois-ci <em>(en effet si on ne lui dit pas, il risque d&rsquo;en faire la remarque pendant le test, ce qui ralentirait son temps de r&eacute;action et donc biaiserait les r&eacute;sultats)</em></li>
                                            </ul>
                                            <p>&nbsp;</p>
                                            <p><span style="text-decoration: underline;"><strong>Cotation&nbsp;: </strong></span></p>
                                            <ul>
                                                <li>Un fichier avec l&rsquo;ensemble des donn&eacute;es est g&eacute;n&eacute;r&eacute; automatiquement</li>
                                            </ul>
                                            <p><strong>&nbsp;</strong></p>
                                            <p><span style="text-decoration: underline;"><strong>R&eacute;cup&eacute;ration des donn&eacute;es&nbsp;: </strong></span></p>
                                            <ul>
                                                <li>T&eacute;l&eacute;charger les fichiers g&eacute;n&eacute;r&eacute;s dans la plateforme</li>
                                            </ul>
