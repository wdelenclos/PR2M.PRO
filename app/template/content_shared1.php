                                        <h4><strong>Guide d&rsquo;utilisation de la plateforme PR2M </strong></h4>
                                        <p><strong>&nbsp;</strong></p>
                                        <p>La plateforme PR2M est un moyen de diffusion et de r&eacute;cup&eacute;ration des donn&eacute;es aupr&egrave;s des praticiens.</p>
                                        <p>Nous avons cr&eacute;&eacute; cet outil sur mesure pour optimiser la mise en application du protocole aupr&egrave;s des professionnels participant &agrave; l&rsquo;&eacute;tude.</p>
                                        <p><strong><u>Espace praticien</u></strong><u>&nbsp;:</u> cr&eacute;er et g&eacute;rer son propre espace de travail</p>
                                        <ul>
                                        <li>Ajouter un patient</li>
                                        <li>Acc&eacute;der au mat&eacute;riel (consignes, test et feuille de passation)</li>
                                        <li>Faire passer la majorit&eacute; des tests directement sur la plateforme</li>
                                        <li>Enregistrer les r&eacute;sultats obtenus dans le dossier de votre patient</li>
                                        <li>Transf&eacute;rer automatiquement vos r&eacute;sultats sur notre base de donn&eacute;es</li>
                                        <li>Observer le suivi de votre patient, sur les trois temps de l&rsquo;&eacute;valuation</li>
                                        <li>Lancer un entra&icirc;nement</li>
                                        <li>Garantir la continuit&eacute; de l&rsquo;entra&icirc;nement &agrave; domicile gr&acirc;ce &agrave; un rapport journalier automatique accessible dans le dossier de votre patient</li>
                                        </ul>
                                        <p>&nbsp;</p>
                                        <p><strong><u>Espace famille</u></strong><u>&nbsp;:</u> suivre facilement son entra&icirc;nement &agrave; domicile</p>
                                        <ul>
                                        <li>Acc&egrave;s personnalis&eacute; &agrave; son entra&icirc;nement <strong>d&rsquo;entra&icirc;nement &agrave; la vitesse lexicale<a href="#_ftn1" name="_ftnref1">[1]</a></strong></li>
                                        <li>Enregistrement automatique de la passation</li>
                                        <li>Rapport journalier d&rsquo;observance automatiquement transf&eacute;r&eacute; sur votre espace de travail</li>
                                        <li>Fonctionne &eacute;galement sur tablette</li>
                                        </ul>
                                        <p>&nbsp;</p>
                                        <p>&nbsp;</p>
                                        <p>L&rsquo;interface du projet est sur <a href="http://pr2m.pro/"><strong>http://pr2m.pro/</strong></a></p>
                                        <p>&nbsp;</p>
                                        <p>&nbsp;</p>
                                        <p>Les pages suivantes vous pr&eacute;sentent le d&eacute;tail du fonctionnement de votre espace de travail, pas &agrave; pas.</p>
                                        <p>&nbsp;</p>
                                        <p><a href="#_ftnref1" name="_ftn1">[1]</a> Seuls les enfants appartenant au <strong>Groupe 1</strong> sont concern&eacute;s par cet espace. Le Groupe 2 fera son entra&icirc;nement sur un autre support (Google Drive) et sera g&eacute;r&eacute; par les auteurs.</p>
